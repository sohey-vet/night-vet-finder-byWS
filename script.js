    showLandingState() {
        this.hideAllStates();
        document.getElementById('landingState').classList.add('active');
    }

    hideAllStates() {
        document.querySelectorAll('.state').forEach(state => {
            state.classList.remove('active');
        });
    }

    // 地名検索を開始
    startLocationNameSearch() {
        const locationInput = document.getElementById('location-input');
        const locationName = locationInput.value.trim();
        
        if (!locationName) {
            alert('地名を入力してください');
            return;
        }

        this.showLoadingState();
        this.searchByLocationName(locationName);
    }

    // 地名から座標を取得して検索
    async searchByLocationName(locationName) {
        try {
            // まずローカルの地名データから検索
            let coordinates = this.getCoordinatesFromLocationName(locationName);
            
            // ローカルデータにない場合はGoogle Geocoding APIを使用
            if (!coordinates && window.googleMapsLoaded) {
                coordinates = await this.geocodeLocation(locationName);
            }
            
            if (!coordinates) {
                this.showErrorState(
                    '指定された地名が見つかりませんでした',
                    '別の地名を試すか、現在地検索をご利用ください。',
                    false
                );
                return;
            }

            this.userLocation = coordinates;
            
            // 病院検索を実行
            if (window.googleMapsLoaded) {
                this.searchNearbyHospitals();
            } else {
                // APIが読み込まれていない場合はモックデータを使用
                setTimeout(() => {
                    this.useMockData();
                }, 1000);
            }
            
        } catch (error) {
            console.error('地名検索エラー:', error);
            this.showErrorState('検索エラー', '検索中にエラーが発生しました。もう一度お試しください。', true);
        }
    }

    // Google Geocoding APIを使用して地名から座標を取得
    async geocodeLocation(locationName) {
        return new Promise((resolve, reject) => {
            const geocoder = new google.maps.Geocoder();
            
            geocoder.geocode(
                { 
                    address: locationName + ', Japan',
                    region: 'JP'
                },
                (results, status) => {
                    if (status === 'OK' && results[0]) {
                        const location = results[0].geometry.location;
                        resolve({
                            lat: location.lat(),
                            lng: location.lng()
                        });
                    } else {
                        console.error('Geocoding failed:', status);
                        resolve(null);
                    }
                }
            );
        });
    }

